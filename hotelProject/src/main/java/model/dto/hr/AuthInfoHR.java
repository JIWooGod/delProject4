package model.dto.hr;

public class AuthInfoHR {
	String authId;
	String authPw;
	String authName;
	String authEmpNo;
	String authTchNo;
	
	public String getAuthId() {
		return authId;
	}
	public void setAuthId(String authId) {
		this.authId = authId;
	}
	public String getAuthPw() {
		return authPw;
	}
	public void setAuthPw(String authPw) {
		this.authPw = authPw;
	}
	public String getAuthName() {
		return authName;
	}
	public void setAuthName(String authName) {
		this.authName = authName;
	}
	public String getAuthEmpNo() {
		return authEmpNo;
	}
	public void setAuthEmpNo(String authEmpNo) {
		this.authEmpNo = authEmpNo;
	}
	public String getAuthTchNo() {
		return authTchNo;
	}
	public void setAuthTchNo(String authTchNo) {
		this.authTchNo = authTchNo;
	}
}
